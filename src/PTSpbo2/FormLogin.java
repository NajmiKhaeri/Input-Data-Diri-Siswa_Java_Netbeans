    public void ceklogin(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/biodata", "root", "");
            
            PreparedStatement ps = con.prepareStatement("SELECT * FROM user Where email=? AND password=?");
            ps.setString(1, email.getText());
            ps.setString(2, String.valueOf(pass.getPassword()));
            ResultSet r = ps.executeQuery();
            if (r.next() == false){
                JOptionPane.showMessageDialog(this,"usename atau password salah");
            } else {
                if(email.getText().equals(r.getString("email")) && pass.getText().equals(r.getString("Password"))){
                FormMenu utama = new FormMenu();
                utama.setLevel(r.getString(1));
                utama.show();
                Luser.setText(r.getString(1));
                Lemail.setText(r.getString(2));
                this.dispose();
                JOptionPane.showMessageDialog(this, "Selamat Datang, Silahkan Input dan Kelola Data");
                }
            }
        } catch (Exception e){
            System.out.println(e);
        }
    }

